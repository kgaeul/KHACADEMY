package com.kh.practice.point.model.vo;

public class Rectangle {

	int width;
	int height;
	
	public Rectangle() {
	}
	public Rectangle(int x,int y,int width,int height) {
		
	}
	public void setWidth(int width) {
		this.width = width;
	}
	public void setHeight(int height) {
		this.height = height;
	}
	public int getWidth() {
		return width;
	}
	public int getHeight() {
		return height;
	}
	
}
