package com.kh.exceptionSample;

import java.util.Scanner;

/*
		 * 
		 throw
		 예외를 발생시키는 키워드
		 프로그램 실행 중 예기치 않은 상황이 발생됐을 때 사용
		 예외를 발생시키면 예외처리가 시작되고 
		 프로그램은 해당예외를 적절히 처리하거나 상위호출 스택예외를 전파
		 
		 user 숫자를 입력해야함
		 양수만 입력해야하는데 음수를 입력함
		 음수인 경우 예외를 발생시키는 간단한 예제
		 
		 
		 */
public class TrowMain {

	public static void main(String[] args) {
		
		
	
		try {
			int number = getNumber(); //숫자를 가지고 옴
			if(number < 0) {
				throw new NegativeNumberException ("음수는 허용이 되지 않습니다.");
			}
			System.out.println("입력한 숫자 : "+number);
		}catch(NegativeNumberException e ){
			System.out.println("예외발생!!!!"+e.getMessage());
		}
	}
		
	public static int getNumber() {
		Scanner sc= new Scanner(System.in);
		System.out.print("숫자를 입력하세요 : ");
		
		int number = sc.nextInt();
			return number;
	}
}
public class NegativeNumberException extends Exception {
	//사용자 정의 예외 클래스
	public NegativeNumberException(String msg) {
		super(msg);
	}
}