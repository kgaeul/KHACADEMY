package com.kh.practice.point.model.vo;

public class Point {

	int x;
	int y;
	
	public Point() {
		
	}
	public Point(int x,int y) {
		
	}
	
	public void setX(int x) {
		this.x = x;
	}
	public void setY(int y) {
		this.y = y;
	}
	public int getX() {
		return x;
	}
	public int getY() {
		return y;
	}
	
}
